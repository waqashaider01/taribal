                            <div class="acc-content">
                                1.	Fixed Rates<br>
2.	Percentage Over Costs<br>
3.	Costs Per Square Metre/Square Foot<br>
4.	Retail Price<br>
5.	Combination Rates<br>
<strong>Fixed Rates</strong> with pre-fixed rates, the client and designer will discuss extensively - as much as is possible - the scope of the works, and an agreeable fee is set.This fixed rate is generally supposed to cover all contingencies and on an agreement, a part of this fee is paid in advance before works commence. The usual amount paid initially may range between 10% and 40%, though this payment is different from a retainer or deposit (a retainer is a certain amount of money paid to a designer to reserve his or her time to work on a project), but whatever the case may be or whichever terminology is preferred, a deposit, retainer or whichever, it will invariably be treated as a form of deposit.<br><br>

<strong>Percentage Over Costs</strong> This method is great for residential projects, though it may be used for commercial projects as well. Charges are the net or wholesale prices that the designer pays to the merchants, vendors, etc., and then a predetermined percentage markup is applied to the net cost.
The markup is on the furniture, furnishings and labour incurred whilst working on a client’s project. The actual net cost is paid to the designer plus a commission which is inclusive of design and planning, selection, delivery and installations.
The percentage charged depends on the nature of works to be executed, which can range from as low as 1% or less, if it’s a commercial contract (usually a large contract), and may go as high as 40% if it’s a small residential project. This means that the percentage charge will vary depending on the size of the project.<br><br>

<strong>Cost Per Square Metre/Square Foot</strong> A very simple way of charging that is commonly used for space planning tasks and is usually a small amount per square meter or footage. What it entails is the interior designer simply measures the client’s space to determine the square meter (or square footage) to be designed. Then multiply by a pre-determined and mutually agreed amount. Space planning is a specialized aspect of interior design and involves the design of space allocation to a person or group of people to work in (or within), so that designated tasks and duties can be performed optimally, with added convenience, efficiency and most especially, comfort.<br><br>

<strong>Combination Rates</strong> This billing method is advised if working on a large project and a complex one. The initial works of space planning can be charged per square metre (or square footage), at a pre-fixed or an hourly rate by the certified interior designer.<br><br>


